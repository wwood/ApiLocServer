  def load_matrix(io)
    column_names = []
    @matrix_hash = {}
    io.each do |line|
      if line.match(/   /)
        column_names = line.strip.split
      elsif line.match(/\#/)
        next #ignore comment lines
      else
        # actual data line
        splut = line.strip.split
        r = {}
        column_names.each_with_index do |col, index|
          r[col] = splut[index+1].to_i
        end
        @matrix_hash[splut[0]] = r
      end
    end
    
    # return self for convenience
    self
  end
  
  def get(column_letter, row_letter)
    # For most matrices the ordering of row, col doesn't matter.
    @matrix_hash[row_letter][column_letter]
  end
  
  def self.blosum62
    d = self.new
    d.load_matrix(BLOSUM62) #load_matrix returns the created distance matrix
  end
end
